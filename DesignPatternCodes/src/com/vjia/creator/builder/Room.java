package com.vjia.creator.builder;

public class Room {

	private String window="";
	private String floor="";
	public Room(){
		
	}
	public String getWindow() {
		return window;
	}
	public void setWindow(String window) {
		this.window = window;
	}
	public String getFloor() {
		return floor;
	}
	public void setFloor(String floor) {
		this.floor = floor;
	}
	
}
